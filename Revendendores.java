package br.com.jairo.modelo;

/**
 *
 * @author Jairo
 */
public class Revendendores {
    private int revCodigo;
    private String revNome;
    private String revNomeCompleto;
    private String revCPF;
    private String revFone;
    private String revEndereco;

    /**
     * @return the revCodigo
     */
    public int getRevCodigo() {
        return revCodigo;
    }

    /**
     * @param revCodigo the revCodigo to set
     */
    public void setRevCodigo(int revCodigo) {
        this.revCodigo = revCodigo;
    }

    /**
     * @return the revNome
     */
    public String getRevNome() {
        return revNome;
    }

    /**
     * @param revNome the revNome to set
     */
    public void setRevNome(String revNome) {
        this.revNome = revNome;
    }

    /**
     * @return the revNomeCompleto
     */
    public String getRevNomeCompleto() {
        return revNomeCompleto;
    }

    /**
     * @param revNomeCompleto the revNomeCompleto to set
     */
    public void setRevNomeCompleto(String revNomeCompleto) {
        this.revNomeCompleto = revNomeCompleto;
    }

    /**
     * @return the revCPF
     */
    public String getRevCPF() {
        return revCPF;
    }

    /**
     * @param revCPF the revCPF to set
     */
    public void setRevCPF(String revCPF) {
        this.revCPF = revCPF;
    }

    /**
     * @return the revFone
     */
    public String getRevFone() {
        return revFone;
    }

    /**
     * @param revFone the revFone to set
     */
    public void setRevFone(String revFone) {
        this.revFone = revFone;
    }

    /**
     * @return the revEndereco
     */
    public String getRevEndereco() {
        return revEndereco;
    }

    /**
     * @param revEndereco the revEndereco to set
     */
    public void setRevEndereco(String revEndereco) {
        this.revEndereco = revEndereco;
    }

}
